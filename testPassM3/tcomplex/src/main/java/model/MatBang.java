package model;

public class MatBang {
	private String maMatBang;
    private String trangThai;
    private float dienTich;
    private int tang;
    private String loai;
    private long giaTien;
    private String ngayBatDau;
    private String ngayKetThuc;

    public String getMaMatBang() { return maMatBang; }
    public void setMaMatBang(String m) { maMatBang = m; }
    public String getTrangThai() { return trangThai; }
    public void setTrangThai(String t) { trangThai = t; }
    public float getDienTich() { return dienTich; }
    public void setDienTich(float d) { dienTich = d; }
    public int getTang() { return tang; }
    public void setTang(int t) { tang = t; }
    public String getLoai() { return loai; }
    public void setLoai(String l) { loai = l; }
    public long getGiaTien() { return giaTien; }
    public void setGiaTien(long g) { giaTien = g; }
    public String getNgayBatDau() { return ngayBatDau; }
    public void setNgayBatDau(String nbd) { ngayBatDau = nbd; }
    public String getNgayKetThuc() { return ngayKetThuc; }
    public void setNgayKetThuc(String nkt) { ngayKetThuc = nkt; }
}
